uint8_t flash_status;
void save_data_to_flash() {
  char buf[MAX_STR_LEN];
  memset(buf, 0, sizeof(buf));

 
  flash_status = FLASH_COMPLETE;
 
  FLASH_Unlock();
  FLASH_ClearFlag(FLASH_FLAG_EOP | FLASH_FLAG_OPERR |FLASH_FLAG_WRPERR | FLASH_FLAG_PGAERR |FLASH_FLAG_PGPERR | FLASH_FLAG_PGSERR);
  flash_status = FLASH_EraseSector(FLASH_Sector_11, VoltageRange_3);
 
  if (flash_status != FLASH_COMPLETE) {
    FLASH_Lock();
    return;
  }
 
  uint8_t* address = &flash_data[0];
 
  uint32_t* data_p;
  uint8_t i;


  data_p = (uint32_t*)&light_timer;
  for (i=0; i<sizeof(light_timer)/4; i++) {
    flash_status = FLASH_ProgramWord((uint32_t)address, (uint32_t)*(data_p+i));
    address += 4;
  }

  data_p = (uint32_t*)&exhaust_setpoints;
  for (i=0; i<sizeof(exhaust_setpoints)/4; i++) {
    flash_status = FLASH_ProgramWord((uint32_t)address, (uint32_t)*(data_p+i));
    address += 4;
  }

  data_p = (uint32_t*)&coolant_setpoints;
  for (i=0; i<sizeof(coolant_setpoints)/4; i++) {
    flash_status = FLASH_ProgramWord((uint32_t)address, (uint32_t)*(data_p+i));
    address += 4;
  }

  data_p = (uint32_t*)&ph_setpoints;
  for (i=0; i<sizeof(ph_setpoints)/4; i++) {
    flash_status = FLASH_ProgramWord((uint32_t)address, (uint32_t)*(data_p+i));
    address += 4;
  }

  data_p = (uint32_t*)&nutrient_pumps;
  for (i=0; i<sizeof(nutrient_pumps)/4; i++) {
    flash_status = FLASH_ProgramWord((uint32_t)address, (uint32_t)*(data_p+i));
    address += 4;
  }

  data_p = (uint32_t*)&misc_settings;
  for (i=0; i<sizeof(misc_settings)/4; i++) {
    flash_status = FLASH_ProgramWord((uint32_t)address, (uint32_t)*(data_p+i));
    address += 4;
  }
  

  if (flash_status != FLASH_COMPLETE) {
    FLASH_Lock();
    return;
  }
  snprintf(buf, MAX_STR_LEN, "{\"event\": \"Saved Data to Flash\", \"time\": \"%s\"}\r\n", global_state.datestring);
  TM_USART_Puts(USART2, buf);
}

void read_flash(void){
  char buf[MAX_STR_LEN];
  memset(buf, 0, sizeof(buf));

  uint8_t* address = &flash_data[0];
 
  if (flash_data[0] == 0xff){
    snprintf(buf, MAX_STR_LEN, "{\"error\": \"Flash empty - Setting Defaults\", \"time\": \"%s\"}\r\n", global_state.datestring);
    set_defaults();
  }else{
    memcpy(&light_timer, address, sizeof(light_timer));
    address += sizeof(light_timer);

    memcpy(&exhaust_setpoints, address, sizeof(exhaust_setpoints));
    address += sizeof(exhaust_setpoints);

    memcpy(&coolant_setpoints, address, sizeof(coolant_setpoints));
    address += sizeof(coolant_setpoints);

    memcpy(&ph_setpoints, address, sizeof(ph_setpoints));
    address += sizeof(ph_setpoints);

    memcpy(&nutrient_pumps, address, sizeof(nutrient_pumps));
    address += sizeof(nutrient_pumps);

    memcpy(&misc_settings, address, sizeof(misc_settings));
    address += sizeof(misc_settings);
    snprintf(buf, MAX_STR_LEN, "{\"event\": \"Restored Data from Flash\", \"time\": \"%s\"}\r\n", global_state.datestring);
  }
  TM_USART_Puts(USART2, buf);
}
