uint8_t flash_status;

void save_data_to_flash(void) {
  char buf[MAX_STR_LEN];
  memset(buf, 0, sizeof(buf));

 
  flash_status = FLASH_COMPLETE;
 
  FLASH_Unlock();
  FLASH_ClearFlag(FLASH_FLAG_EOP | FLASH_FLAG_OPERR |FLASH_FLAG_WRPERR | FLASH_FLAG_PGAERR |FLASH_FLAG_PGPERR | FLASH_FLAG_PGSERR);
  flash_status = FLASH_EraseSector(FLASH_Sector_11, VoltageRange_3);
 
  if (flash_status != FLASH_COMPLETE) {
    FLASH_Lock();
    return;
  }
 
  uint8_t* address = &flash_data[0];
 
  uint32_t* data_p;
  uint8_t i;


  data_p = (uint32_t*)&timer_events;
  for (i=0; i < sizeof(timer_events)/4; i++) {
    flash_status = FLASH_ProgramWord((uint32_t)address, (uint32_t)*(data_p+i));
    address += 4;
  }

  if (flash_status != FLASH_COMPLETE) {
    FLASH_Lock();
    return;
  }
  snprintf(buf, MAX_STR_LEN, "{\"event\": \"Saved Data to Flash\"}\r\n");
  TM_USART_Puts(USART2, buf);
}

void read_flash(void){
  char buf[MAX_STR_LEN];
  memset(buf, 0, sizeof(buf));

  uint8_t* address = &flash_data[0];
 
  if (flash_data[0] == 0xff){
    snprintf(buf, MAX_STR_LEN, "{\"error\": \"Flash empty - Setting Defaults\"}\r\n");
    set_defaults();
  }else{
    memcpy(&timer_events, address, sizeof(timer_events));
    address += sizeof(timer_events);
    snprintf(buf, MAX_STR_LEN, "{\"event\": \"Restored Data from Flash\"}\r\n");
  }
  TM_USART_Puts(USART2, buf);
}

